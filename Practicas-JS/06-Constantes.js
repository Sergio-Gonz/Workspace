//constantes en Javascript
//una constante es un valor que no cambia durante la ejecución de un programa
//se declaran con la palabra reservada const y en mayúsculas

const MI_CONSTANTE = 10; //se declara la constante MI_CONSTANTE con el valor 10

// MI_CONSTANTE = 20; //esto dará un error, ya que no se puede cambiar el valor de una constante

console.log(MI_CONSTANTE); //esto imprimirá 10 en la consola

//Las constantes son útiles para definir valores que no deben cambiar durante la ejecución de un programa, como por ejemplo el valor de PI en matemáticas o el número de días en una semana.

console.log(Math.PI); //esto imprimirá el valor de PI en la consola


// constante de segundos por minuto

const SEGUNDOS_POR_MINUTO = 60;
console.log(SEGUNDOS_POR_MINUTO); //esto imprimirá 60 en la consola

